   <b>Jak zostać programistą?</b> <br>
   Trzeba ćwiczyć, ćwiczyć i ćwiczyć. Stawiać przed sobą wyzwania, rozwiązywać problemy i cierpliwie „klepać” linijki kodów. Po czasie sami stwierdzicie, że „nie taki diabeł straszny…”. <br/>

<b>Programowanie</b>, to rozwiązywanie istniejącego problemu i zapisywania tego rozwiązania w postaci zrozumiałej dla komputera. Schemat działań, jakie należy podjąć w celu rozwiązania problemu, to nic innego jak algorytm.

Programy komputerowe są wszędzie – samochody, pralki, lodówki, inteligentne domy… można wyliczać bez końca. Jak to działa? Wszystko co wprowadzamy do komputera, czy jest to tekst, czy obrazek musi zostać jakoś przez maszynę odczytane. Komputer nie „widzi” jak my – on przetwarza. Ale co? ciągi zer i jedynek – system binarny.

Za pomocą zer i jedynek można zapisać wszystko – zainteresowanych pogłębianiem tematu odsyłam do bardzo fajnego artykułu – KLIK

Programowanie jest cyklicznym procesem tworzenia kodu w następujących etapach:
<ul>
    <li>1. edycji kodu źródłowego,</li>
    <li>2. uruchamiania programu,</li>
    <li>3. analizie działania i rejestrowania błędów,</li>
    <li>4. powrocie do edycji kodu źródłowego (pkt 1) w celu poprawienia błędów lub dalszego poszerzania funkcjonalności.</li>
    <li>5. Osobę zajmującą się programowaniem nazywa się programistą.</li>
</ul>

<p><b>Algorytm</b> to zestaw instrukcji, czynności, które należy wykonać, aby zrealizować pewne zadanie w skończonej liczbie kroków.</p>

